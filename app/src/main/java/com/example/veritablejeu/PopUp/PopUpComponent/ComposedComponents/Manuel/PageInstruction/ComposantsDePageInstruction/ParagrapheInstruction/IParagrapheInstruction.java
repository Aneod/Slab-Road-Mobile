package com.example.veritablejeu.PopUp.PopUpComponent.ComposedComponents.Manuel.PageInstruction.ComposantsDePageInstruction.ParagrapheInstruction;

public interface IParagrapheInstruction {

    /**
     * Renvoie la hauteur actuelle de la classe.
     * @return un int.
     */
    int getHauteur();

    /**
     * Modifie la hauteur de la classe.
     * @param hauteur la nouvelle hauteur.
     */
    void setHauteur(int hauteur);

    /**
     * Renvoie la marge supérieure actuelle de la classe.
     * @return un int.
     */
    int getTopMargin();

    /**
     * Modifie la marge supérieure de la classe.
     * @param topMargin la nouvelle marge supérieure.
     */
    void setTopMargin(int topMargin);
}
